window.currentLang = 'de';

function setLanguage(lang) {
    if (!i18nDict[lang]) return;
    window.currentLang = lang;
    
    // Update translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18nDict[lang][key]) {
            // Check if element has nodes inside to preserve (like icons in buttons)
            const textNodes = Array.from(el.childNodes).filter(node => node.nodeType === Node.TEXT_NODE);
            if (textNodes.length > 0) {
                // Simplified approach: just replace text content directly, 
                // but for complex buttons like the drive ones we used span for icons
                if (el.querySelector('span')) {
                    el.childNodes.forEach(n => {
                        if (n.nodeType === Node.TEXT_NODE && n.textContent.trim().length > 0) {
                            n.textContent = ' ' + i18nDict[lang][key];
                        }
                    });
                } else {
                    el.textContent = i18nDict[lang][key];
                }
            } else {
                el.textContent = i18nDict[lang][key];
            }
        }
    });
    
    // Update language toggle buttons UI
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    document.getElementById(`lang-${lang}`).classList.add('active');
}

// Global exposure
window.i18nDict = i18nDict;
window.setLanguage = setLanguage;
